    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            frmCliente fm = new frmCliente();
            PessoaRN rn = new PessoaRN();
            JFormattedTextField cpf = new JFormattedTextField();
            mask.maskCPF(cpf);
            JLabel rotulo = new JLabel("Digite CPF:");
            JPanel tela = new JPanel();
            tela.add(rotulo);
            tela.add(cpf);
            JOptionPane.showMessageDialog(rootPane, tela, "Pesquisar Cliente Cadastrado", JOptionPane.QUESTION_MESSAGE);
            
            Pessoa cli = rn.trazCadastro(cpf.getText());
            if (cli == null) {
                JOptionPane.showMessageDialog(frmOsPrincipal.this, "CPF invÃ¡lido", "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                fm.clientePesquisado(cli);
                fm.setVisible(true);
                fm.setLocationRelativeTo(null);
                dispose();
            }
        } catch (ParseException ex) {
            Logger.getLogger(frmOsPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

